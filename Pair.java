import java.util.*;
public class Pair
{
  private int x, y;
  public Pair()
  {
    x = 0;
    y = 0;
  }
  public Pair(int n, int m)
  {
    x = x;
    y = y;
  }

  public void setPair(int n,int m)
  {
    x = n;
    y = m;
  }
  public int getX()
  {
    return x;
  }
  public int getY()
  {
    return y;
  }
}
